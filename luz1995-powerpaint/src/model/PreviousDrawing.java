package model;

import java.awt.Color;
import java.awt.Shape;

/**
 * a class that to store drawing information.
 * 
 * @author Zhou Lu
 * @version 11/22/2017
 */
public class PreviousDrawing {
    
    /** a field to store the shape.*/
    private final Shape myShape;
    
    /** a field to store the Width.*/
    private final int myWidth;
    
    /** a field to store the color.*/
    private final Color myColor;
    
    /** a field to store the fill color.*/
    private final Color myFillColor;
    
    /** a field to indicate the shape is filled or not.*/
    private final boolean myFillOrNot;
    
    /**
    private boolean myFillOrNot;
    
    /**
     * a constructor.
     * @param theShape the shape the drawing have.
     * @param theWidth the width the drawing have
     * @param theColor the color the drawing have.
     * @param theFillColor the color the drawing have.
     * @param theFillOrNot the boolean value to check fill drawing.
     */
    public PreviousDrawing(final Shape theShape, final int theWidth, 
                           final Color theColor, final Color theFillColor, 
                           final boolean theFillOrNot) {
        myShape = theShape;
        myWidth = theWidth;
        myColor = theColor;
        myFillColor = theFillColor;
        myFillOrNot = theFillOrNot;
    }
    
    /**
     * a get method of shape.
     * @return return the drawing shape.
     */
    public Shape getShape() {
        return myShape;
    }
    
    /**
     * a get method of width.
     * @return return the drawing width.
     */
    public int getWidth() {
        return myWidth;
    }
    
    /**
     * a get method of color.
     * @return return the drawing color.
     */
    public Color getColor() {
        return myColor;
    }
    
    /**
     * a get method of fill color.
     * @return return the drawing fill color.
     */
    public Color getFillColor() {
        return myFillColor;
    }
    
    /**
     * a get method of boolean fillOrNot.
     * @return return the status of fill or not.
     */
    public boolean isFillOrNot() {
        return myFillOrNot;
    }
}
